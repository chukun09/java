package studentpractice.NguyenPhiTruong.BST_Hashtable;

public class Word {
	public String en;
	public String vi;
	
	public Word(String word) {
		this.en = word;
	}
	
	public String getEn() {
		return this.en;
	}
	
	public String getVi() {
		return this.vi;
	}
	
	public void setVi(String vi) {
		this.vi = vi;
	}
}
